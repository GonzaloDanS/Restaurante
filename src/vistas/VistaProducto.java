    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        try {
            if (jtfBuscar.getText().equals("Ingrese el código del producto.") || jtfBuscar.getText().isEmpty()) {
                cargarTabla();
                JOptionPane.showMessageDialog(this, "El campo no puede estar vacío.", "Campo vacío.", JOptionPane.WARNING_MESSAGE);
            } else {
                int id = Integer.parseInt(jtfBuscar.getText());
                Producto p = pd.buscarProducto(id);
                int e;
                if (p != null) {
                    if (p.isEstado()) {
                        e = 1;
                    } else {
                        e = 0;
                    }
                    borrarFilas();
                    modelo.addRow(new Object[]{p.getIdProd(), p.getNom(), p.getPrecio(), p.getStock(), e});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error. Únicamente debe ingresar dígitos. | " + e.getLocalizedMessage(), "Formato erróneo.", JOptionPane.ERROR_MESSAGE);
            cargarTabla();
        }
    }//GEN-LAST:event_jbBuscarActionPerformed


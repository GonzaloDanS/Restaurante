    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        int f = jtProductos.getSelectedRow();
        int res = JOptionPane.showConfirmDialog(this, "¿Desea eliminar los productos seleccionados?", "Eliminar producto", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
                if (jtProductos.isCellSelected(i, 0)) {
                    pd.eliminarProducto(Integer.parseInt(modelo.getValueAt(i, 0).toString()));
                }
            }
            cargarTabla();
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        int res = JOptionPane.showConfirmDialog(this, "¿Desea modificar los productos?", "Modificar producto", JOptionPane.YES_NO_OPTION);
        try {
            if (res == JOptionPane.YES_OPTION) {
                for (int i = 0; i < modelo.getRowCount(); i++) {
                    boolean x;
                    if (Integer.parseInt(modelo.getValueAt(i, 4).toString()) == 1) {
                        x = true;
                    } else {
                        x = false;
                    }
                    Producto p = new Producto(Integer.parseInt(modelo.getValueAt(i, 0).toString()), modelo.getValueAt(i, 1).toString(), Double.parseDouble(modelo.getValueAt(i, 2).toString()), Integer.parseInt(modelo.getValueAt(i, 3).toString()), x);
                    pd.modificarProducto(p);
                }
                cargarTabla();
                JOptionPane.showMessageDialog(this, "Modificado exitósamente.", "Modificación completa.", JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "La modificación se ha cancelado.", "Modificación cancelada.", JOptionPane.PLAIN_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error. Formato no admitido. | " + e.getLocalizedMessage(), "Formato erróneo.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbModificarActionPerformed

